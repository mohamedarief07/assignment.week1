package assignment.week1;

public class Calculator {
	/*
	 * (a) add(int num1, int num2, int num3), it should return sum of num1+num2+num3
	 * (b) sub(int num1, int num2), it should return subtraction of of num1-num2 (c)
	 * mul(double num1, double num2), it should return product of num1 * num2 (d)
	 * divide(float num1, float num2), it should return division of num1 / num2
	 */
	
	public int add(int num1, int num2, int num3) {
	return num1+num2+num3;
	}
	public int sub(int num1, int num2) {
		return num1-num2;
		}
	public double mul(double num1, double num2) {
		return (num1*num2);
		}

	public float divide(float num1, float num2) {
		return (num1/num2);
		}




}
